    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed

        int num = (Integer) this.spnNum.getValue();
        int numAux = num;

        int auxNum = 0;

        while (num / 10 > 0) {

            auxNum *= 10;
            auxNum += num % 10;

            num = num / 10;
        }

        auxNum *= 10;
        auxNum += num % 10;

        if (auxNum == numAux) {
            this.txtSol.setText("ES UN NUMERO CAPICUA");
        } else {
            this.txtSol.setText("NO ES UN NUMERO CAPICUA");
        }


    }//GEN-LAST:event_btnGenerarActionPerformed


