    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        
        int seg = Integer.parseInt(this.txtSeg.getText());
        
        int min = 0;
        int h = 0;
        
        if (seg > 59) {
            
            min = seg / 60;
            seg = seg % 60;
            
        }
        
        if (min > 59) {
            
            h = min / 60;
            min = min % 60;
        }
        
        this.txtSol.setText("Horas : " + h + " / Minutos : " + min + " / Segundos : " + seg);
        
    }//GEN-LAST:event_btnGenerarActionPerformed


