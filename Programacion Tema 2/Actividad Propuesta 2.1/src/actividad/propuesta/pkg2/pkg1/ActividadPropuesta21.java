//Realiza un programa que almacene la evaluacion de distintas expresiones relacionales 
//en variables booleanas , y muestra el valor de dichas variables.




public class ActividadPropuesta21 {

    
    public static void main(String[] args) {
        
        
        int a = 10;
        int b = 8;
        
        boolean ev = a < b ;
        boolean ev1 = a > b;
        boolean ev2 = a * b == 80;
        
        System.out.println("Primera Ev : " + ev);
        System.out.println("Segunda Ev : " + ev1);
        System.out.println("Tercera Ev : " + ev2);
        
        
    }
    
}
